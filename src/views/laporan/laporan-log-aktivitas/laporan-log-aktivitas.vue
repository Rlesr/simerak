<script lang="ts">
    import { defineComponent } from 'vue';
    import Layout from '@/components/layout/Layout.vue';

    interface DataItem {
        tanggalAktivitas: string;
        nrk: string;
        namaPengguna: string;
        namaOpd: string;
        Aktivitas: string;
    }

    export default defineComponent({
        name: 'laporanLlogAktivitas',
        components: {
            Layout,
        },
        data() {
            return {
                data: [
                    {
